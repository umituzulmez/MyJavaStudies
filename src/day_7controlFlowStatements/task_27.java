public class task_27 {

	public static void main(String[] args) {
		
		int hour=13;
		
		if (hour<12) {
			String s1="Good Morning";
			
			System.out.println(s1);
			
		}

		
		if (hour>=12 && hour<15) {
			String s2="Good Afternoon";
			
			System.out.println(s2);
	}
		
		if (hour>=15) {
			String s3="Good Evening";
			
			System.out.println(s3);
			
		}
		
		}

}
